package org.java.pojo;
/**
 * 这个类用来统计每个人被表白的次数，产生热度榜
 * @author DELL
 *
 */
public class Famous {
	private int n;//被表白的次数
	private String name;//用户名字
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
